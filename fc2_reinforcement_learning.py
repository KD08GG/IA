class WarehouseEnv(gym.Env):
    def __init__(self, grid_size=10):
        super(WarehouseEnv, self).__init__()
        self.delivered_packages = 0  # Contador de paquetes entregados


        # Tama帽o de la cuadr铆cula
        self.grid_size = grid_size

        #Random obstacle
        x_r, y_r = random_obstacle()
        print("Random obstacle generated at [", x_r,",", y_r, "]")

        # Configuraci贸n del almac茅n
        self.obstacles = {(3, 3), (5,5), (x_r,y_r)}
        self.pickup_point = (9, 4)
        self.delivery_point = (8, 8)

        # Espacios de acci贸n y observaci贸n
        self.action_space = spaces.Discrete(6)  # 0-3 (mover), 4 (recoger), 5 (entregar)
        self.observation_space = spaces.Box(
            low=np.array([0, 0, 0, 0]),
            high=np.array([grid_size - 1, grid_size - 1, 1, 100]),
            dtype=np.int32
        )

        # Estado inicial
        self.reset()

    def reset(self):
        """Reinicia el entorno y devuelve el estado inicial."""
        self.robot_pos = [1, 1]  # Posici贸n inicial del robot
        self.has_item = 0        # Estado del 铆tem (0 = no tiene, 1 = tiene)
        self.energy = 100         # Energ铆a inicial
        #x_r, y_r = random_obstacle()
        #print("Reset Random obstacle generated at [", x_r,",", y_r, "]")
        return np.array(self.robot_pos + [self.has_item, self.energy])

    def step(self, action):
        """Ejecuta una acci贸n y devuelve (estado, recompensa, done, info)."""

        # Si la energ铆a est谩 en 0, termina el episodio autom谩ticamente
        if self.energy == 0:
            return np.array(self.robot_pos + [self.has_item, self.energy]), -5, True, {}

        # Inicializar recompensa y done
        reward = -1  # Penalizaci贸n base por movimiento
        done = False

        # Guardar posici贸n previa
        prev_pos = self.robot_pos.copy()

        # Desglosar la acci贸n
        if action in {0, 1, 2, 3}:  # Acciones de movimiento
            self.move(action)

        # Si el robot choca con un obst谩culo, no se mueve y recibe penalizaci贸n
        if tuple(self.robot_pos) in self.obstacles:
            self.robot_pos = prev_pos  # Regresa a la posici贸n anterior
            reward = -10  # Penalizaci贸n por colisi贸n

        elif action == 4:  # Recoger 铆tem
            if tuple(self.robot_pos) == self.pickup_point and self.has_item == 0:
                self.has_item = 1
                #reward = 5  # Bonificaci贸n por recoger

        elif action == 5:  # Entregar 铆tem
            if tuple(self.robot_pos) == self.delivery_point and self.has_item == 1:
                reward = 10
                self.has_item = 0  # Suelta el paquete, pero sigue jugando
                self.delivered_packages += 1  # Incrementar el contador

        # Reducir energ铆a solo si no ha terminado el episodio
        self.energy -= 1
        if self.energy == 0:  # Si la energ铆a llega a 0, termina el episodio
            reward = -5
            done = True

        # Retornar estado, recompensa, done, info
        return np.array(self.robot_pos + [self.has_item, self.energy]), reward, done, {}

    def move(self, action):
        """Mueve al robot en la direcci贸n dada (0 = arriba, 1 = abajo, 2 = izquierda, 3 = derecha)."""
        x, y = self.robot_pos

        if action == 0:  # Arriba
            y = max(0, y - 1)
        elif action == 1:  # Abajo
            y = min(self.grid_size - 1, y + 1)
        elif action == 2:  # Izquierda
            x = max(0, x - 1)
        elif action == 3:  # Derecha
            x = min(self.grid_size - 1, x + 1)

        self.robot_pos = [x, y]  # Mueve al robot

"""## Implementaci贸n del Agente de Aprendizaje por Refuerzo"""

import random
import numpy as np
from collections import defaultdict

class QLearningAgent:
    def __init__(self, env, alpha=0.99, gamma=0.8, epsilon=1.0, epsilon_decay=0.98, epsilon_min=0.01):
        self.env = env
        self.q_table = defaultdict(lambda: np.zeros(env.action_space.n))
        self.alpha = alpha  # Tasa de aprendizaje
        self.gamma = gamma  # Factor de descuento
        self.epsilon = epsilon  # Probabilidad inicial de exploraci贸n
        self.epsilon_decay = epsilon_decay  # Factor de reducci贸n de exploraci贸n
        self.epsilon_min = epsilon_min  # Valor m铆nimo de epsilon

    def choose_action(self, state):
        if random.uniform(0, 1) < self.epsilon:
            return self.env.action_space.sample()  # Exploraci贸n
        else:
            return np.argmax(self.q_table[tuple(state)])  # Explotaci贸n

    def update_q_table(self, state, action, reward, next_state):
        old_value = self.q_table[tuple(state)][action]
        next_max = np.max(self.q_table[tuple(next_state)])
        new_value = (1 - self.alpha) * old_value + self.alpha * (reward + self.gamma * next_max)
        self.q_table[tuple(state)][action] = new_value

    def update_epsilon(self):
        self.epsilon = max(self.epsilon_min, self.epsilon * self.epsilon_decay)

